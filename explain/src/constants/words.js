export function getRandomWord() {
  const allWords = [...EASY_WORDS, ...MEDIUM_WORDS, ...HARD_WORDS];
  return allWords[Math.floor(Math.random() * allWords.length)];
}


export function getRandomWordByDifficulty(difficulty) {
  const wordLists = {
    easy: EASY_WORDS,
    medium: MEDIUM_WORDS,
    hard: HARD_WORDS,
  };

  const words = wordLists[difficulty] || MEDIUM_WORDS;
  return words[Math.floor(Math.random() * words.length)];
}

export function getWordCounts() {
  return {
    easy: EASY_WORDS.length,
    medium: MEDIUM_WORDS.length,
    hard: HARD_WORDS.length,
    total: EASY_WORDS.length + MEDIUM_WORDS.length + HARD_WORDS.length,
  };
}
